async function callEndpoint(baseUrl, token) {
  const cases = [
    { name: "empty-hasRotation", hasRotation: "", subscribe: "Yes - send me candidate updates" },
    { name: "invalid-hasRotation", hasRotation: "maybe", subscribe: "yes" },
    { name: "explicit-false", hasRotation: "no", subscribe: "no" },
  ];

  console.log(`Calling ${baseUrl}/api/save-employer-request\n`);
  for (const testCase of cases) {
    const payload = buildPayload(token, testCase.hasRotation, testCase.subscribe);
    const response = await fetch(`${baseUrl}/api/save-employer-request`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(payload),
    });

    let body = null;
    try {
      body = await response.json();
    } catch {
      body = "Non-JSON response";
    }

    console.log(`[${testCase.name}] status=${response.status}`);
    console.log(`  hasRotation="${testCase.hasRotation}" subscribe="${testCase.subscribe}"`);
    console.log(`  response=${JSON.stringify(body)}\n`);
  }
}

